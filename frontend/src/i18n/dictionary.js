export const DEFAULT_LANG = 'ru';

export function getValue(lang, path) {
  const parts = path.split('.');
  let cur = DICT[lang] || DICT[DEFAULT_LANG];
  for (const p of parts) {
    if (cur == null) return path;
    cur = cur[p];
  }
  if (cur == null && lang !== DEFAULT_LANG) return getValue(DEFAULT_LANG, path);
  return cur ?? path;
}
